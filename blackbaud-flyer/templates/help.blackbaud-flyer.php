        <div class="panel-heading">
            <h3 class="panel-title">What does the Blackbaud Flyers plugin do?</h3>
        </div>
        <div class="panel-body">
            <p>The Flyer plugin creates the HTML (and CSS/JS) necessary for a fully-functional Twitter Boostrap Modals (or popups). Flyers are added to the page in the form of shortcodes.</p>
            <h4>Features:</h4>
            <ul class="bulleted-list">
                <li>Shortcodes to output a fully-featured modal</li>
                <li>Editor button to allow you to easily add a modal to your posts and pages</li>
                <li>Each modal has configurable options for:
                    <ul class="bulleted-list">
                        <li>Title</li>
                        <li>Body content</li>
                        <li>Image</li>
                        <li>Excerpt</li>
                        <li>Call to action button</li>
                        <li>Image as flyer background</li>
                        <li>Launch automatically when page loads</li>
                        <li>Launch automatically only once</li>
                        <li>Launcher button</li>
                        <li>Custom CSS class</li>
                        <li>Custom HTML</li>
                    </ul>
                </li>
            </ul>
        </div>
    </div>
    <div class="panel">
        <div class="panel-heading">
            <h3 class="panel-title">Updating the plugin's global settings</h3>
        </div>
        <div class="panel-body">
            <ol class="numbered-list">
                <li>Go to Flyers > Settings</li>
                <li>If you want to style your flyers from scratch, uncheck the "Include default style sheet"</li>
                <li>If your theme already includes the Bootstrap CSS/JS files, uncheck "Include Twitter Bootstrap"</li>
                <li>Click "Save Changes"</li>
            </ol>
        </div>
    </div>
    <div class="panel">
        <div class="panel-heading">
            <h3 class="panel-title">How to create a new flyer</h3>
        </div>
        <div class="panel-body">
            <ol class="numbered-list">
                <li>Go to Flyer > Add New</li>
                <li>Fill in the form's fields, as needed</li>
                <li>Click the "Publish" button</li>
            </ol>
        </div>
    </div>
    <div class="panel">
        <div class="panel-heading">
            <h3 class="panel-title">How to add a flyer to a post or page</h3>
        </div>
        <div class="panel-body">
            <ol class="numbered-list">
                <li>Edit the post or page that will display a Flyer (or, create a new post or page).</li>
                <li>Determine where you want your flyer to be placed by clicking somewhere in the main Content Area.</li>
                <li>Next, click on the Content Area's "Flyer" editor button and select a flyer from the dropdown.</li>
                <li>You should now see a <a href="https://en.support.wordpress.com/shortcodes/" target="_blank">WordPress Shortcode</a> in the Content Area representing your Flyer.</li>
                <li>Save the post/page and preview it, making sure that the flyer appears correctly.</li>
            </ol>
        </div>
