    <div class="panel">
        <div class="panel-heading">
            <h3 class="panel-title">What does the Blackbaud Luminate Survey plugin do?</h3>
        </div>
        <div class="panel-body">
            <p>The Luminate Survey plugin adds API Survey forms to your WordPress site. Luminate Forms are added to the page in the form of shortcodes.</p>
            <h4>Features:</h4>
            <ul class="bulleted-list">
                <li>Shortcodes to output Luminate Survey forms</li>
                <li>Editor button to allow you to easily add a form to your posts and pages</li>
            </ul>
        </div>
    </div>
    <div class="panel">
        <div class="panel-heading">
            <h3 class="panel-title">Configuring WordPress to communicate with Luminate API's</h3>
        </div>
        <div class="panel-body">
            <ol class="numbered-list">
                <li>Go to Surveys > Settings</li>
                <li>Fill in each field based on the information provided in your Luminate Online's Site Options page.</li>
                <li>Click "Save Changes"</li>
            </ol>
        </div>
    </div>
    <div class="panel">
        <div class="panel-heading">
            <h3 class="panel-title">How to create a new survey</h3>
        </div>
        <div class="panel-body">
            <ol class="numbered-list">
                <li>Go to Survey > Add New</li>
                <li>Fill in the Survey ID derived from an actual Luminate Online Survey</li>
                <li>Click the "Publish" button</li>
            </ol>
        </div>
    </div>
    <div class="panel">
        <div class="panel-heading">
            <h3 class="panel-title">How to add a survey to a post or page</h3>
        </div>
        <div class="panel-body">
            <ol class="numbered-list">
                <li>Go to Surveys</li>
                <li>Next to the desired survey, you should see a shortcode. Copy this code to your clipboard.</li>
                <li>Edit the post or page that will display a survey (or, create a new post or page).</li>
                <li>Determine where you want your survey to be placed by clicking somewhere in the main Content Area.</li>
                <li>Next, paste the shortcode you copied from Step 2 into the Content Area.</li>
                <li>Save the post/page and preview it, making sure that the survey appears correctly.</li>
            </ol>
        </div>
    </div>
